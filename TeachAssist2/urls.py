    path('project/', include('project_management.urls')),

    path('doc2/', schema_view.with_ui('swagger', cache_timeout=None), name='schema-swagger-ui'),
    path('doc1/', schema_view.with_ui('redoc', cache_timeout=None), name='schema-redoc'),

